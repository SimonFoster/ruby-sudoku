  def self.empty
    (1..81).map { |c| Array (1..9) }
  end
  
 def SudokuSolver.from_s s
    bd = empty
    81.times do |i|
      j = s.scan(/./).map { |c| c.to_i }
      if j[i] != 0 then assign( bd, i, j[i] ) end
    end
    bd
  end
  
  def self.to_s bd
    bd.map { |c| c[0].to_s }.join
  end

  def self.solve bd
    if complete? bd
      p to_s bd
    else
      next_boards( bd ) { |b| solve b }
    end
  end
  
  def self.complete? bd
    bd.all? { |c| c.length == 1 }
  end

  def self.assign( bd, pos, val )
    # Assign val at pos and eliminate from peers
    # return false if there is a contradiction
    bd[pos] = [val]
    eliminate_from_peers( bd, pos, val )
  end

  def self.eliminate_from_peers( bd, pos, val )
    # Eliminate val from all cells peered with pos
    # Return false if any block invocation is false-ish
    PEERS[pos].all? { |p| eliminate( bd, p, val ) }
  end
  
  def self.eliminate( bd, pos, val )
    # Eliminate val from cell specified by pos
    # and propagate if # remaining values == 1
    # return false if a contradiction is detected
    if bd[pos].delete val
      case bd[pos].length
        when 0 
          return false  # an empty cell is a always a contradiction
        when 1
          return eliminate_from_peers( bd, pos, bd[pos][0] )
      end
    end
    return true
  end

  def self.next_boards bd
    # Produce boards based on the current position by guessing
    # Choose a position with the minimum # of remaining values to
    # increase the chance of guessing correctly
    mrv = minimum_remaining_values bd
    bd[mrv].each do |guess|
      newbd = bd.map( &:dup )
      assign( newbd, mrv, guess ) and yield newbd
    end
  end
  
  def SudokuSolver.minimum_remaining_values bd
    min_remain = bd.select { |c| c.length > 1 }.min_by( &:length ).length
    bd.index { |c| c.length == min_remain }
  end

end

